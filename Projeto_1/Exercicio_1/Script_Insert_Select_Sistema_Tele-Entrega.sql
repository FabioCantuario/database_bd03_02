SELECT c.id as Código,c.nome as Cliente,c.fone as Telefone,e.logradouro as Logradouro,e.numero as Número,b.nome as Bairro
 FROM endereco e
 INNER JOIN cliente c ON  c.id = e.clienteId
 INNER JOIN bairro b ON b.id = e.bairroId;
 
SELECT f.id, f.nome, f.fone, f.cargoId, cg.descricao
  FROM funcionario f
  INNER JOIN cargo cg ON cg.id = f.cargoId
  WHERE cargoId=2;
  
SELECT p.id as 'Código', p.sabor as 'Pizza', p.tamanho as 'Tam', p.valorUn as 'Valor Unitário', i.descricao as 'Ingrediente' 
   FROM pizza p
   INNER JOIN ingrediente i ON i.id = p.ingredienteId
   WHERE p.sabor='Calabresa' AND p.tamanho='GG';

SELECT pd.id as 'Pedido', pd.dtEmissao as 'Data/Hora Emissão', c.nome as 'Cliente',c.fone as 'Cli_Fone', e.logradouro as 'Logradouro', e.numero as 'Nr', e.pontoreferencia as 'Ponto Ref.', b.nome as 'Bairro', 
	   z.nome as 'Zona', f.nome as 'Entregador',f.fone as 'Fone_Entregador',fn.nome as 'Emitente', pd.entrega as 'Entrega', pd.valorTotal as 'Valor Total', b.tempo_espera as 'Tempo de Espera' 
   FROM pedido pd
   INNER JOIN cliente c ON c.id = pd.clienteId
   INNER JOIN endereco e ON e.id = pd.endCliId
   INNER JOIN bairro b ON b.id = e.bairroId
   INNER JOIN zona z ON z.id = b.zonaId
   INNER JOIN funcionario f ON f.id = pd.funcEntregaId
   INNER JOIN funcionario fn ON fn.id = pd.funcEmiteId;
   
SELECT pdi.id as 'Item', p.id as 'Nr_Pedido', pz.id as 'Cód_Pizza', pz.sabor as 'Sabor', pz.tamanho as 'TAM.', pz.valorUn as 'Valor_Un', pdi.qtdPizza as 'QTD', (pz.valorUn * pdi.qtdPizza) as subTotal
   FROM pedido_item pdi
   INNER JOIN pedido p ON p.id = pdi.pedidoId
   INNER JOIN pizza pz ON pz.id = pdi.pizzaId;
   
SELECT card.tpCardapio as 'Tipo Cardapio', card.id as 'Item', pz.sabor as 'Sabores', i.descricao as 'Ingrediente', pz.tamanho as 'TAM', pz.valorUn as 'Valor'
   FROM cardapio card
   INNER JOIN pizza pz ON pz.id = card.pizzaId
   INNER JOIN ingrediente i ON i.id = pz.ingredienteId;   